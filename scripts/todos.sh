#!/bin/sh

################################################################################
#
# A script to find all of the todos that still exist in the Thesis.
#
# Usage:
#     ./todos.sh
#
################################################################################

#===============================================================================
# Configuration
#===============================================================================
DOC_ROOT="$(dirname $0)/../src"
#-------------------------------------------------------------------------------

